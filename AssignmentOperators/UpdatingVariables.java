
public class UpdatingVariables {
    public static void main(String[] args) {
        
    // meaning gumawa ako ng variable named number and nag lagay ako ng 67 sa loob nito and pinatawag gamit and print

        int number = 67;

        System.out.println(number);

    // and dito inupdate ko yung laman ng variable "number"

        number = 76;

        System.out.println(number);



    }
    
}
