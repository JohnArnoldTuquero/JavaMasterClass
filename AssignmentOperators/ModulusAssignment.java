
public class ModulusAssignment {
    public static void main(String[] args) {
        
        //gumawa ako ng variable name "number" and may laman na 8 and ni print siya
        int number = 8;

        System.out.println(number);


        //now para ma update yung laman niya is ni-call ko ulit yung variable and para malaman yung remainder gagamitin yung /= and lagay ng digit kung ilan remainder
        number %= 8;

        System.out.println(number);




    }
}
